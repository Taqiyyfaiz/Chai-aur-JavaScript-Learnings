const month = "March"

switch (month) {
    case "Jan":
        console.log("January");
        break; 
    case 'Feb':
        console.log("Feb");
        break;
    case "March":
        console.log("March");
        break;
    case "April":
        console.log("April");
        break;

    default:
        console.log("default case match");
        break;
}